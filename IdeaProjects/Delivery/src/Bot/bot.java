package Bot;

public class bot {

    int ip;
    int id;

  public bot(int ip, int id) {
    this.ip = ip;
    this.id = id;
    }

    public int getIp() {
        return ip;
    }

    public void setIp(int ip) {
        this.ip = ip;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


}